import re
def clean(item):
  result = item.replace("$", "") 
  result = result.replace('"', '')
  result = result.replace("[","")
  result = result.replace("]","")
  result = result.replace("'","")
  result = result.encode("utf-8")
  result = re.sub(r'\xe2\x80\x93','',result,re.U)
  result = re.sub(r'\xe2\x80\x99','',result,re.U)
  result = re.sub(r'\xc3\xa1','',result,re.U)
  result = re.sub(r'\xc3\xa9','',result,re.U)
  result = re.sub(r'\xc3\xa2','',result,re.U)
  result = re.sub(r'\xef\xbd\x9e','',result,re.U) 
  result = re.sub(r'\xe3\x80\x90','',result,re.U) 
  result = re.sub(r'\xef\xbc\x81\xe3\x80\x91','',result,re.U) 
  result = re.sub(r'\xef\xbc\x8a','',result,re.U)
  result = re.sub(r'\xe2\x80\x98','',result,re.U)
  result = re.sub(r'\xe2\x80\x9c','',result,re.U)
  result = re.sub(r'\xc3\x97','',result,re.U)
  result = re.sub(r'\xe2\x99\xa5','',result,re.U)
  result = re.sub(r'\xe3\x83\x94\xe3\x83\xb3\xe3\x82\xbf\xe3\x83\x83\xe3\x82\xaf\xe3\x83\x96\xe3\x83\xa9\xe3\x82\xa6\xe3\x82\xb9','',result,re.U)
  result = re.sub(r'\xe3\x83\x89\xe3\x83\x83\xe3\x83\x88\xe3\x83\x81\xe3\x83\xa5\xe3\x83\xbc\xe3\x83\xab\xe3\x82\xab\xe3\x83\x83\xe3\x83\x88\xe3\x83\x9c\xe3\x83\xac\xe3\x83\xad','',result,re.U)
  result = re.sub(r'\xe3\x82\xaa\xe3\x83\xbc\xe3\x83\x90\xe3\x83\xbc\xe3\x83\x8b\xe3\x83\xbc ','',result,re.U)
  result = re.sub(r'\xe7\xb4\xa0\xe6\x95\xb5\xe3\x81\xaa\xe3\x83\x97\xe3\x83\xaa\xe3\x83\xb3\xe3\x82\xbb\xe3\x82\xb9\xe3\x83\x96\xe3\x83\xa9\xe3\x82\xa6\xe3\x82\xb9','',result,re.U)
  result = re.sub(r'\xe3\x81\x8a\xe8\x8f\x93\xe5\xad\x90\xe3\x81\xae\xe5\x9b\xbd','',result,re.U) 
  return result 
